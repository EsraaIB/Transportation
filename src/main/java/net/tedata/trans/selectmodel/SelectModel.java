package net.tedata.trans.selectmodel;

public class SelectModel {
	private String selectedBusNo,
	        selectedBranch,
	        selectedBusTime;
	public String getSelectedBusNo() {
		return selectedBusNo;
	}
	public void setSelectedBusNo(String selectedBusNo) {
		this.selectedBusNo = selectedBusNo;
	}
	public String getSelectedBranch() {
		return selectedBranch;
	}
	public void setSelectedBranch(String selectedBranch) {
		this.selectedBranch = selectedBranch;
	}
	public String getSelectedBusTime() {
		return selectedBusTime;
	}
	public void setSelectedBusTime(String selectedBusTime) {
		this.selectedBusTime = selectedBusTime;
	}

}
